package Lesson_7.Task2;
public class Computer1 {
    private String processor;
    private int ram;
    private int hdc;

    public Computer1(String processor, int ram, int hdc) {
        this.processor = processor;
        this.ram = ram;
        this.hdc = hdc;
    }

    public String getProcessor1() {
        return processor;
    }

    public void setProcessor1(String processor) {
        this.processor = processor;
    }

    public int getRam1() {
        return ram;
    }

    public void setRam1(int ram) {
        this.ram = ram;
    }

    public int getHdc1() {
        return hdc;
    }

    public void setHdc1(int hdc) {
        this.hdc = hdc;
    }

}
